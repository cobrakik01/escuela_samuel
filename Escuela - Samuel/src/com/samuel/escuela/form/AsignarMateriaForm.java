    private void txtClaveProfesorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtClaveProfesorKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            ProfesorJpaController controlador = new ProfesorJpaController();
            try {
                Profesor profesor = controlador.findProfesor(Integer.parseInt(txtClaveProfesor.getText()));
                if (profesor != null) {
                    llenarCamposProfesor(profesor);
                } else {
                    limpiarCamposProfesor();
                    mensajeError(txtClaveProfesor, "No se encontraron resultados");
                }
            } catch (Exception e) {
                limpiarCamposProfesor();
                mensajeError(txtClaveProfesor, "Error de formato");
            }
        }
    }//GEN-LAST:event_txtClaveProfesorKeyReleased

