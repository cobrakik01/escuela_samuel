    public void mensajeError(JTextField txt, String mensaje) {
        Toolkit.getDefaultToolkit().beep();
        limpiarCamposProfesor();
        txt.setText(mensaje);
        txt.requestFocus();
        txt.selectAll();
    }

    public void limpiarCamposProfesor() {
        txtNombre.setText("");
        txtApellidoPaterno.setText("");
        txtApellidoMaterno.setText("");
    }

    public void llenarCamposProfesor(Profesor profesor) {
        txtNombre.setText(profesor.getNombre());
        txtApellidoPaterno.setText(profesor.getApellidoPaterno());
        txtApellidoMaterno.setText(profesor.getApellidoMaterno());
    }

